static inline void mthp_exp_inc(enum mthp_exp_counter counter)
{
	atomic64_inc(&mthp_exp_counters[counter]);
}

static inline void mthp_exp_add(enum mthp_exp_counter counter, long value)
{
	atomic64_add(value, &mthp_exp_counters[counter]);
}

